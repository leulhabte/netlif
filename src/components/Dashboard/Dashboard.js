export default function Dashboard() {
  const dispatch = useDispatch();
  useEffect(() => {
    dispatch(getusers());
  }, dispatch);
  const classes = useStyles();
  const [open, setOpen] = React.useState(true);
  const handleDrawerOpen = () => {
    setOpen(true);
  };
  const handleDrawerClose = () => {
    setOpen(false);
  };
  const fixedHeightPaper = clsx(classes.paper, classes.fixedHeight);

  const history = useHistory();
  const [user, setUser] = useState(JSON.parse(localStorage.getItem('profile')));

  const Refresh = () => {
    setTimeout("location.reload(true);", 0);
  }
  const logout = () => {

    dispatch({ type: actionType.LOGOUT });


    setUser(null);
    history.push('/');
    Refresh();
  };

  const [product, setproduct] = React.useState('');

  const handleChange = (event) => {
    setproduct(event.target.value);
  };

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar position="absolute" className={clsx(classes.appBar, open && classes.appBarShift)}>
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(classes.menuButton, open && classes.menuButtonHidden)}
          >
            <MenuIcon />
          </IconButton>
          <Typography component="h1" variant="h6" color="inherit" noWrap className={classes.title}>
            Dashboard
          </Typography>
          <a href='/add-product'>
            <Button variant="contained" className={classes.buttons}>Add Product</Button>
          </a>
          <Button variant="contained" onClick={logout} style={{ margin: '10px' }}>Logout</Button>
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
          <IconButton onClick={handleDrawerClose}>
            <ChevronLeftIcon />
          </IconButton>
        </div>
        <Divider />
        <List>{mainListItems}</List>
        <Divider />
        <Grid>
          <FormControl fullWidth>
            <InputLabel id="demo-simple-select-label">product</InputLabel>
            <Select
              labelId="demo-simple-select-label"
              id="demo-simple-select"
              value={product}
              label="product"
              onChange={handleChange}
            >
              <MenuItem value={10}>Add Product</MenuItem>
              <MenuItem value={20}>Remove Product</MenuItem>
              <MenuItem value={30}>Update Product</MenuItem>
            </Select>
          </FormControl>
        </Grid>


        {/* <List>{secondaryListItems}</List> */}
      </Drawer>
      <main className={classes.content}>
        <div className={classes.appBarSpacer} />
        <Container maxWidth="lg" className={classes.container}>
          <Grid container spacing={3}>
            {/* Chart */}
            <Grid item xs={12} md={8} lg={9}>
              <Paper className={fixedHeightPaper}>
                <Chart />
              </Paper>
            </Grid>
            <Grid item xs={12}>
              <Paper className={classes.paper}>
                <Customers />
              </Paper>
            </Grid>
            <Grid item xs={12}>
              <Paper className={classes.paper}>
                <Orders />
              </Paper>
            </Grid>

          </Grid>
          <Box pt={4}>
            <Copyright />
          </Box>
        </Container>
      </main>

    </div>
  );
}